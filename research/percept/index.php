                <p class="researchParagraph center" style="float: none;">We performed a perception study to examine the human's ability to perceive relative depth of stereoscopically and monoscopically rendered two-dimensional shapes. Participants were required to estimate the relative distance ratios of shapes with respect to a reference object.
                    <br><br>
                    To display stereoscopic 3D content, a TFT-LCD monitor, model ZM-M240W, by Zalman Tech. Co., Ltd. was used. It is a passive stereoscopic 3D LCD monitor requiring polarized 3D glasses. The screen resolution was set to 1920x1080. Questions were displayed on a second monitor, and could be answered by using a mouse. 
                   </p>
                <p class="researchParagraph center" style="float:none;">
                   To avoid accidental motion parallax, blurring effects, as well as fluctuations in perceived depths, each participant was required to place their head on a chin rest. While seated, the distance from each participant's forehead was made to be exactly 1 metre away from the base of the 3D monitor.</p>
                    <div style="margin: 20px 215px;">
                    Materials used:
                <ul>
                    <li>Passive stereoscopic 3D LCD monitor</li>
                    <li>Polarized 3D glasses</li>
                    <li>3D Content</li>
                    <li>Chin-rest</li></ul>
                </div>
                   

                        Our hypotheses:<br>
                        <ul>
                            <li>Stereoscopic viewing condition would improve relative depth perception compared to the monoscopic viewing condition</li>
                            <li>Estimating relative depth distances would have an accuracy of fifty percent or less in both viewing conditions</li>
                            <li>Shape type would not affect relative depth estimation accuracy</li>
                            <li>Confidence levels would be higher for the stereoscopic viewing condition</li>
                        </ul>
                    

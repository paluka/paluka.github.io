                <p class="researchParagraph center" style="float: none;">Embellished visualizations have fostered an ongoing debate regarding the use of decorative graphics in visualization design, and their impact on data readability and visualization memorability. However, little is known about how embellishments added to data visualizations impact the emotion of human viewers. 
                    <br><br>
                    In this work, we conducted a study to compare how humans affectively respond to embellished and plain, minimalistic chart visualizations. Using electrodermal response (EDR) sensors and two psychological questionnaires, we measured the physiological and psychological responses of human subjects while they were exposed to 36 different visualization stimuli. 
                    

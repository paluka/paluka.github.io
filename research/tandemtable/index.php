                <p class="researchParagraph bold upFont" style="margin-left: 20px;">Study 1:</p>
                <p class="researchParagraph">Conducted a participatory observational study of a tandem language learning environment (Conversation Caf√©), as well as interviewed its administrator and four participants.
                </p>
            </div>
            <div class="researchSection">
                <img class="researchImg" id="TLLImg" src="../../img/TandemTable/TLL-small.jpg" alt="TandemTable" width="305" height="249">
                <p class="researchParagraph bold upFont">TandemTable:</p>
                <p class="researchParagraph">Designed and implemented a computer-assisted language learning system called TandemTable. Designed for two co-located tandem language learners, the system suggests and grounds conversations on user-specified themes/topics, and provides digital media artifacts that contain up-to-date content delivered straight from the web. Web services, such as Bing Translator, Diffbot, Feedzilla, YouTube, Twitter and Flickr, were harnessed to achieve TandemTable's functionality.
                </p>
            </div>
            <div class="researchSection">
                <img class="researchImg" id="TLLStudyImg" src="../../img/TandemTable/Study.jpeg" alt="TandemTable Study 2" width="300" height="401">
                <p class="researchParagraph bold upFont">Study 2:</p>
                <p class="researchParagraph">Conducted an exploratory evaluation study with university undergraduate and graduate students to evaluate TandemTable. Subjective data was gathered through pre- and post-interviews and questionnaires, including Likert scales, along with observational and interaction data.</p> 
            </div>
            
            <div class="researchSection" style="margin-top: 200px;">
                <img class="researchImg" id="TLLMicImg" src="../../img/TandemTable/microphone.jpg" alt="Microphone" width="300" height="250">
                <p class="researchParagraph bold upFont">Refinement:</p>
                <p class="researchParagraph">With the insights gained from the exploratory evaluation, TandemTable was refined, which among others, included adding voice activity detection (VAD) by adding microphone support and implementing digital signal processing (DSP) algorithms.</p> 
            </div>
            <div class="researchSection">
                <img class="researchImg" id="TLLStudyImg" src="../../img/TandemTable/tll_in_use.jpg" alt="TandemTable Study 3" width="300" height="229">
                <p class="researchParagraph bold upFont">Study 3:</p>
                <p class="researchParagraph">Evaluated TandemTable quantitatively with a within-subject comparative study. Language learners' utterance rates were measured and compared between conditions. The conditions involved employing TandemTable as a learning aid, as well as having no computer-assistance which is the standard method in tamdem language learning.</p> 
            </div>
            

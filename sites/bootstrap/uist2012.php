                       <p class="columns">In the summer of 2012, my team entered the ACM Symposium on User Interface Software and Technology's (UIST) Student Innovation Contest. The goal of the contest was to create novel interactions on state-of-the-art hardware. Synaptics provided us with a pre-production trackpad prototype, called the ForcePad, that can detect pressure and location for up to five fingers. With the ForcePad, my team created an application prototype called "Multiple Independent Semantic Text Selection Using Pressure", which allows users to quickly select text content based on semantics and the amount of pressure applied. On October 8, 2012, I presented my team's demo at UIST 2012 in Cambridge, Massachusetts, USA.</p>

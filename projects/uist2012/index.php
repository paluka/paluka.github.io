        <div style="margin: auto; width: 400px; text-align: justify; max-height: 20000px;"> In the summer of 2012, my team entered the ACM Symposium on User Interface Software and Technology's (UIST) Student Innovation Contest. The goal of the contest was to create novel interactions on state-of-the-art hardware. Synaptics provided us with a pre-production trackpad prototype, called the ForcePad, that can detect pressure and location for up to five fingers. 
            
                    <img class="researchImg" id="forcePadImg" src="../../img/projects/ForcePad.jpg" alt="ForcePad" width="305" height="250">
            
            With the ForcePad, my team created an application prototype called "<span class="bold">Multiple Independent Semantic Text Selection Using Pressure</span>", which allows users to quickly, independently and simultaneously select multiple text regions based on touch pressure and semantics. Depending on the placement of the on-screen cursors and text content, the application would select words, sentences or paragraph sections based on the amount of pressure applied. On October 8, 2012, I presented my team's demo at UIST 2012 in Cambridge, Massachusetts, USA.
               

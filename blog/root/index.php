                    <h2>ODIN &amp; Recovery Manager</h2>
                        For Samsung device owners, you can use ODIN on Windows to flash your smartphone and install a recovery manager. Doing so will allow you to easily wipe your phone and install a custom Android ROM. Created by Samsung and subsequently leaked to the public, <a href="http://forum.xda-developers.com/showthread.php?t=1738841" target="_blank" title="ODIN">ODIN</a> is software that allows one to flash firmware on their Samsung device. Remember to check the legality in your region before doing so.              

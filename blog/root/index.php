                   For example, if you have the Samsung Galaxy S2 HD LTE (Model SGH-I757M), @titanic_fanatic from xda-developers.com has <a href="http://forum.xda-developers.com/galaxy-s2/general/rom-unofficial-carbon-kitkat-bell-sgh-t2661144" target="_blank" title="Carbon ROM">customized his own version of the Carbon ROM</a> for this model. Based on technology from Carbon, CyanogenMod, SlimRoms, AOKP and Paranoid Android, you can get a special flavour of the Android KitKat 4.4.x OS for this device.
